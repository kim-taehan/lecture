public class 피자배달거리_14 {

    static int N, M;
    static int[][] graph;

    static List<Node> pizza = new ArrayList<>();
    static List<Node> house= new ArrayList<>();

    static List<Set<Node>> pizzaCombi = new ArrayList<>();
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        N = scanner.nextInt();
        M = scanner.nextInt();
        graph = new int[N][N];
        for (int i = 0; i < N; i++) {
            for (int j = 0; j < N; j++) {
                int value = scanner.nextInt();
                if(value == 1) house.add(new Node(j, i));
                else if(value == 2) pizza.add(new Node(j, i));

                graph[i][j] = value;
            }
        }

        // 피자집을 M으로 나누는 조합을 먼저 구한다.
        dfs(0, 0, new int[M]);

        int ret = Integer.MAX_VALUE;
        // 피자집을 순차적으로 돌려면 최소길이을 계산한다.
        for (Set<Node> nodes : pizzaCombi) {
            // 피자집을 없앤 구조로 변경한다. 폐업된 피자 0, 살아남은 피자집 2

            int sum = 0;
            for (Node house : house) {
                sum += bfs(house, nodes);
            }

            ret = Math.min(ret, sum);

        }
        System.out.println(ret);

    }

    static int bfs(Node house, Set<Node> nodes) {
        int minDis = Integer.MAX_VALUE;
        for (Node node : nodes) {
            int distance = Math.abs(node.x - house.x) + Math.abs(node.y - house.y);
            minDis = Math.min(minDis, distance);
        }
        return minDis;
    }

    static void dfs(int depth, int index, int[] combi) {

        if (depth == M) {
            Set<Node> nodes = new HashSet<>();
            for (int i = 0; i < M; i++) {
                nodes.add(pizza.get(combi[i]));
            }
            pizzaCombi.add(nodes);
        } else {
            for (int i = index; i < pizza.size(); i++) {
                combi[depth] = i;
                dfs(depth+1, i+1, combi);
            }
        }
    }

    static class Node {
        int x, y;

        public Node(int x, int y) {
            this.x = x;
            this.y = y;
        }
    }
}
